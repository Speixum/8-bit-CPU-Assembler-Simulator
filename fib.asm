; ============================================
; 8位CPU斐波那契数列 (完整可运行)
; 输出：0,1,1,2,3,5...233，溢出停机
; ============================================

; 初始化输出 0
LDI  0
STA  0xFF   ; 打印0

; 初始化输出 1
LDI  1
STA  0x80   ; 当前值 = 1
STA  0xFF   ; 打印1

LDI  0
STA  0x81   ; 前一个值 = 0

; 主循环：计算下一项
loop:
LDA  0x80   ; A = 当前值
TAB         ; B = 当前值
ADD  0x81   ; A = 当前值 + 前一个值
JC   done   ; 进位溢出 → 结束

STA  0xFF   ; 打印新值
STA  0x80   ; 更新当前值
TBA         ; A = 旧当前值
STA  0x81   ; 更新前一个值
JMP  loop   ; 循环

; 结束停机
done:
HLT